/** \brief Recibe 2 operandos, los suma, y devuelve el resultado.
 *
 * \param a int El primer sumando
 * \param b int El segundo sumando
 * \return int La suma
 *
 */
int suma(int a, int b);


/** \brief Recibe 2 operandos, resta el primero por el segundo, y devuelve el resultado.
 *
 * \param a int El minuendo
 * \param b int El sustraendo
 * \return int La diferencia
 *
 */
int resta(int a, int b);


/** \brief Recibe 2 operandos, divide el primero por el segundo, y devuelve el resultado.
 *
 * \param a int El dividendo
 * \param b int El divisor
 * \return float El cociente (muestra 2 decimales)
 *
 */
float division(int a, int b);


/** \brief Recibe 2 operandos, los multiplica, y devuelve el resultado.
 *
 * \param a int Un factor
 * \param b int Otro factor
 * \return int El producto
 *
 */
int multiplicacion(int a, int b);


/** \brief Recibe un operando y devuelve su factorial.
 *
 * \param a int El operando
 * \return long long Su factorial
 *
 */
long long factorial(int a);
